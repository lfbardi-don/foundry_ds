/// Foundation tokens for border widths.
/// These values ensure consistent border styling across the design system.
class FBorderWidth {
  FBorderWidth._();

  /// No border (0px)
  static const double none = 0.0;

  /// Hairline border (1px) - for subtle separators
  static const double hairline = 1.0;

  /// Thin border (1.5px) - for inputs and cards
  static const double thin = 1.5;

  /// Medium border (2px) - for focus rings and emphasis
  static const double medium = 2.0;

  /// Thick border (3px) - for strong emphasis
  static const double thick = 3.0;
}
