/// Foundation tokens for interactive control component sizes.
///
/// These values ensure consistent sizing across checkbox, radio, switch,
/// progress, and avatar components.
class FControlSize {
  FControlSize._();

  /// Standard control size for checkbox and radio (20px)
  static const double controlMd = 20.0;

  /// Inner fill size for radio buttons (10px)
  static const double controlInner = 10.0;

  /// Switch track width (44px)
  static const double switchTrackWidth = 44.0;

  /// Switch track height (24px)
  static const double switchTrackHeight = 24.0;

  /// Switch thumb diameter (20px)
  static const double switchThumb = 20.0;

  /// Switch thumb padding from track edge (2px)
  static const double switchThumbPadding = 2.0;

  /// Linear progress bar height (4px)
  static const double progressHeight = 4.0;

  // Avatar sizes
  /// Extra small avatar (24px)
  static const double avatarXs = 24.0;

  /// Small avatar (32px)
  static const double avatarSm = 32.0;

  /// Medium avatar (40px) - default
  static const double avatarMd = 40.0;

  /// Large avatar (48px)
  static const double avatarLg = 48.0;

  /// Extra large avatar (64px)
  static const double avatarXl = 64.0;

  // Avatar status indicator sizes
  /// Status indicator for xs avatar (6px)
  static const double statusXs = 6.0;

  /// Status indicator for sm avatar (8px)
  static const double statusSm = 8.0;

  /// Status indicator for md avatar (10px)
  static const double statusMd = 10.0;

  /// Status indicator for lg avatar (12px)
  static const double statusLg = 12.0;

  /// Status indicator for xl avatar (14px)
  static const double statusXl = 14.0;

  // Avatar initials font sizes
  /// Font size for xs avatar initials (10px)
  static const double avatarFontXs = 10.0;

  /// Font size for sm avatar initials (12px)
  static const double avatarFontSm = 12.0;

  /// Font size for md avatar initials (14px)
  static const double avatarFontMd = 14.0;

  /// Font size for lg avatar initials (16px)
  static const double avatarFontLg = 16.0;

  /// Font size for xl avatar initials (20px)
  static const double avatarFontXl = 20.0;

  // Bottom sheet handle
  /// Width of bottom sheet drag handle (36px)
  static const double handleWidth = 36.0;

  /// Height of bottom sheet drag handle (4px)
  static const double handleHeight = 4.0;

  // Checkbox/Radio icon size
  /// Icon size inside checkbox/radio controls (16px)
  static const double checkboxIconSize = 16.0;
}
