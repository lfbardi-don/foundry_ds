/// Foundation tokens for icon sizes.
/// These values ensure consistent iconography across the design system.
class FIconSize {
  FIconSize._();

  /// Extra small icon (12px) - for inline indicators
  static const double xs = 12.0;

  /// Small icon (16px) - for compact UI elements
  static const double sm = 16.0;

  /// Medium icon (20px) - default size for most contexts
  static const double md = 20.0;

  /// Large icon (24px) - for prominent actions
  static const double lg = 24.0;

  /// Extra large icon (32px) - for hero elements
  static const double xl = 32.0;

  /// Extra extra large icon (48px) - for illustrations
  static const double xxl = 48.0;
}
